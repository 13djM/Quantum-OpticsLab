document.addEventListener('DOMContentLoaded', () => {
    loadPublications();
});

let allPubs = publicationsData; // Use the local data

function loadPublications() {
    const list = document.getElementById('pub-list');
    const filterContainer = document.getElementById('year-filters');

    try {
        // Use local data instead of fetch
        allPubs = publicationsData;

        // Render Filters
        const years = [...new Set(allPubs.map(p => p.year))].sort((a, b) => b - a);
        years.forEach(year => {
            const btn = document.createElement('button');
            btn.className = 'filter-btn';
            btn.textContent = year;
            btn.dataset.year = year;
            btn.addEventListener('click', () => filterPubs(year, btn));
            filterContainer.appendChild(btn);
        });

        // Add filter listener for 'All'
        const allBtn = filterContainer.querySelector('[data-year="all"]');
        if (allBtn) {
            allBtn.addEventListener('click', (e) => filterPubs('all', e.target));
        }

        // Initial Render
        renderPubs(allPubs);

    } catch (error) {
        console.error(error);
        list.innerHTML = 'Error loading publications.';
    }
}

function filterPubs(year, btnEl) {
    // Update active state
    document.querySelectorAll('.filter-btn').forEach(b => b.classList.remove('active'));
    btnEl.classList.add('active');

    // Filter
    const filtered = year === 'all' ? allPubs : allPubs.filter(p => p.year == year);
    renderPubs(filtered);
}

function renderPubs(pubs) {
    const list = document.getElementById('pub-list');
    list.innerHTML = '';

    if (pubs.length === 0) {
        list.innerHTML = '<div style="text-align:center;">No publications found for this criteria.</div>';
        return;
    }

    pubs.forEach(pub => {
        const item = document.createElement('article');
        item.className = 'pub-item';

        let metaHtml = `<span class="pub-year">${pub.year}</span>`;
        if (pub.journal) metaHtml += `<span>${pub.journal} <b>${pub.volume || ''}</b>, ${pub.pages || ''}</span>`;
        if (pub.doi) metaHtml += `<a href="https://doi.org/${pub.doi}" target="_blank" style="margin-left:auto; color:var(--color-primary); font-weight:600;">DOI</a>`;
        else if (pub.arxiv) metaHtml += `<a href="https://arxiv.org/abs/${pub.arxiv}" target="_blank" style="margin-left:auto; color:var(--color-primary); font-weight:600;">arXiv:${pub.arxiv}</a>`;

        item.innerHTML = `
            <div class="pub-title">${pub.title}</div>
            <div class="pub-authors">${pub.authors}</div>
            <div class="pub-meta">${metaHtml}</div>
        `;
        list.appendChild(item);
    });
}
